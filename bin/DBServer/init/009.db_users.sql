#AGGIORNAMENTO PASSWORD ROOT
CREATE USER IF NOT EXISTS 'root'@'127.0.0.1' IDENTIFIED BY 'mitrol2021';
CREATE USER IF NOT EXISTS 'root'@'localhost' IDENTIFIED BY 'mitrol2021';
ALTER USER `root`@`127.0.0.1` IDENTIFIED BY 'mitrol2021';
ALTER USER `root`@`localhost` IDENTIFIED BY 'mitrol2021';
GRANT ALL PRIVILEGES ON *.* TO 'root'@'127.0.0.1' IDENTIFIED BY 'mitrol2021';
GRANT ALL PRIVILEGES ON *.* TO 'root'@'localhost' IDENTIFIED BY 'mitrol2021';

#CREAZIONE UTENTE ADMIN
CREATE USER IF NOT EXISTS `admin`@`localhost`;
GRANT SHUTDOWN ON *.* to `admin`@`localhost`;

